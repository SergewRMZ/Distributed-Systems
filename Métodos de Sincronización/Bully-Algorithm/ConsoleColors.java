public class ConsoleColors {
    public static final String RESET = "\033[0m";  // Text Reset

    public static final String RED = "\033[0;31m";     // ROJO
    public static final String GREEN = "\033[0;32m";   // VERDE
    public static final String YELLOW = "\033[0;33m";  // AMARILLO
    public static final String BLUE = "\033[0;34m";    // AZUL
    public static final String MAGENTA = "\033[0;35m"; // MAGENTA
    public static final String CYAN = "\033[0;36m";    // CYAN

    public static final String RED_BOLD = "\033[1;31m";    // ROJO BOLD
    public static final String GREEN_BOLD = "\033[1;32m";  // VERDE BOLD
    public static final String YELLOW_BOLD = "\033[1;33m"; // AMARILLO BOLD
}